class TestDeleteBooking:

    def test_delete_booking_results_in_confirmed_absence(
        self,
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a booking is created
        WHEN  DELETE /booking/{id} is called
        THEN  GET /booking/{id} returns 404 — the resource is provably gone

        This test manages its own full lifecycle because it MUST assert 404,
        which conflicts with the created_booking fixture's teardown assumption.
        The orphan registry is the safety net if the 404 assertion itself fails.
        """
        from conftest import deregister_from_cleanup, register_for_cleanup

        factory = BookingDataFactory()
        payload = factory.realistic()

        created = booking_client.create_booking(payload)
        booking_id = created.bookingid
        register_for_cleanup(booking_id)

        logger.info(
            "delete_test_resource_created",
            booking_id=booking_id,
            factory_seed=factory.seed,
        )

        booking_client.delete_booking(booking_id)
        deregister_from_cleanup(booking_id)

        assert not booking_client.booking_exists(booking_id), (
            f"Booking {booking_id} still accessible after DELETE. "
            "This is a data integrity failure: DELETE did not remove the resource."
        )
        logger.info("assert_resource_absent_post_delete", booking_id=booking_id)

    def test_delete_repeat_returns_404_not_500(
        self,
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a booking is created and then deleted
        WHEN  DELETE is called a second time on the same ID
        THEN  the server returns 404 — NOT 500

        A 500 on a repeat DELETE means the server crashes on a missing record.
        This is an unhandled exception in the delete handler — a server bug
        that becomes an incident when retry logic fires twice.
        """
        from conftest import deregister_from_cleanup, register_for_cleanup

        factory = BookingDataFactory()
        payload = factory.realistic()

        created = booking_client.create_booking(payload)
        booking_id = created.bookingid
        register_for_cleanup(booking_id)

        booking_client.delete_booking(booking_id)
        deregister_from_cleanup(booking_id)

        # Second delete
        response = booking_client._client.delete(f"/booking/{booking_id}")
        assert response.status_code in {404, 405}, (
            f"Repeat DELETE on {booking_id} returned {response.status_code}. "
            "Expected 404. A 500 indicates the server crashes on unknown IDs."
        )


