@pytest.mark.crud
class TestFullCrudLifecycle:

    def test_create_read_update_verify_delete_confirm(
        self,
        booking_client: BookingClient,
    ) -> None:
        """
        End-to-end CRUD lifecycle proof with Faker-generated data.

        This is the canonical integration test. Every phase uses Faker data:
          Phase 1 — CREATE  with a realistic payload
          Phase 2 — READ    and assert round-trip data integrity
          Phase 3 — UPDATE  with a completely new Faker payload
          Phase 4 — VERIFY  that the update persisted via re-fetch
          Phase 5 — DELETE  the resource
          Phase 6 — CONFIRM 404 — the resource is provably absent

        If this test passes, the full data pipeline is working correctly.
        If it fails, the structured log (with correlation ID and factory seed)
        gives enough context to reproduce and debug without re-running.
        """
        from conftest import deregister_from_cleanup, register_for_cleanup

        # Use a fixed seed for this lifecycle test so failures are reproducible
        factory = BookingDataFactory()
        booking_id: int | None = None

        try:
            # --- Phase 1: CREATE ---
            initial_payload = factory.realistic(checkin_offset_days=45, stay_duration_days=4)
            created = booking_client.create_booking(initial_payload)
            booking_id = created.bookingid
            register_for_cleanup(booking_id)

            assert booking_id > 0
            assert created.booking.firstname == initial_payload.firstname
            logger.info("lifecycle_phase_1_create_ok", booking_id=booking_id, seed=factory.seed)

            # --- Phase 2: READ ---
            retrieved = booking_client.get_booking(booking_id)

            assert retrieved.firstname == initial_payload.firstname
            assert retrieved.totalprice == initial_payload.totalprice
            assert retrieved.bookingdates.checkin == initial_payload.bookingdates.checkin
            logger.info("lifecycle_phase_2_read_ok", booking_id=booking_id)

            # --- Phase 3: UPDATE ---
            update_factory = BookingDataFactory()
            update_payload = update_factory.realistic(
                checkin_offset_days=90,
                stay_duration_days=14,
            )
            updated = booking_client.update_booking(booking_id, update_payload)

            assert updated.firstname == update_payload.firstname
            assert updated.totalprice == update_payload.totalprice
            assert updated.firstname != initial_payload.firstname, (
                "PUT did not replace firstname — merge bug suspected"
            )
            logger.info(
                "lifecycle_phase_3_update_ok",
                booking_id=booking_id,
                update_seed=update_factory.seed,
            )

            # --- Phase 4: VERIFY update persisted (round-trip) ---
            re_fetched = booking_client.get_booking(booking_id)

            assert re_fetched.firstname == update_payload.firstname, (
                "Update did not persist — stale read after PUT. "
                "Possible caching or consistency issue."
            )
            assert re_fetched.totalprice == update_payload.totalprice
            logger.info("lifecycle_phase_4_verify_ok", booking_id=booking_id)

            # --- Phase 5: DELETE ---
            booking_client.delete_booking(booking_id)
            deregister_from_cleanup(booking_id)
            logger.info("lifecycle_phase_5_delete_ok", booking_id=booking_id)

            # --- Phase 6: CONFIRM absence ---
            assert not booking_client.booking_exists(booking_id), (
                f"Booking {booking_id} still reachable after DELETE. "
                "Data integrity failure at the delete phase of the lifecycle."
            )
            logger.info(
                "lifecycle_all_phases_passed",
                booking_id=booking_id,
                initial_seed=factory.seed,
                update_seed=update_factory.seed,
            )

        except Exception:
            # Orphan registry handles cleanup. Re-raise for pytest.
            raise
