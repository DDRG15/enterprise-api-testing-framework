@pytest.mark.crud
class TestPatchBooking:

    def test_patch_updates_only_specified_fields(
        self,
        created_booking: tuple[int, BookingPayload],
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN an existing booking with known field values
        WHEN  PATCH is called with ONLY totalprice and depositpaid
        THEN  those two fields reflect new values
        AND   firstname, lastname, and dates are UNCHANGED

        Silently nulling unspecified fields is the most dangerous form of
        PATCH bug. In a financial system, it can zero out payment amounts
        or erase customer names without any error signal.
        """
        booking_id, original_payload = created_booking

        patch_data = {"totalprice": 9999, "depositpaid": False}
        patched = booking_client.partial_update_booking(booking_id, patch_data)

        # Patched fields must reflect new values
        assert patched.totalprice == 9999, (
            f"PATCH: totalprice expected 9999, got {patched.totalprice}"
        )
        assert patched.depositpaid is False, (
            f"PATCH: depositpaid expected False, got {patched.depositpaid}"
        )

        # Untouched fields must be byte-for-byte identical
        assert patched.firstname == original_payload.firstname, (
            f"PATCH corrupted firstname. "
            f"Expected '{original_payload.firstname}', got '{patched.firstname}'. "
            "PATCH must not modify unspecified fields."
        )
        assert patched.lastname == original_payload.lastname, (
            f"PATCH corrupted lastname."
        )
        assert patched.bookingdates.checkin == original_payload.bookingdates.checkin, (
            "PATCH modified checkin date — should not have touched date fields."
        )

        logger.info(
            "assert_patch_field_isolation_passed",
            booking_id=booking_id,
        )

    def test_patch_additionalneeds_to_null(
        self,
        created_booking: tuple[int, BookingPayload],
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a booking with an additionalneeds value
        WHEN  PATCH sets additionalneeds to empty string
        THEN  the field is updated and no other fields are affected

        Nullable optional fields have a second bug class: setting them
        to empty/null may raise a 400 or be silently ignored.
        """
        booking_id, original_payload = created_booking

        patch_data = {"additionalneeds": ""}
        patched = booking_client.partial_update_booking(booking_id, patch_data)

        # Core fields must be untouched
        assert patched.firstname == original_payload.firstname
        assert patched.totalprice == original_payload.totalprice


