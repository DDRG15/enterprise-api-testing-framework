"""
tests/functional/test_bookings_crud.py
========================================
Full CRUD lifecycle tests using Faker-generated synthetic data.

Data strategy:
  - Every payload is generated by BookingDataFactory, which embeds a UUID
    fragment in the lastname and the run_id in additionalneeds.
  - This guarantees zero collision across parallel workers, repeated runs,
    and shared staging environments — without sacrificing data realism.
  - The factory seed is logged on every test so any failure is 100%
    reproducible: grab the seed from the CI log, re-run locally.

Test philosophy:
  - Each test is INDEPENDENT. No shared mutable state between tests.
  - `created_booking` fixture guarantees teardown regardless of outcome.
  - Re-running this suite 100 times leaves the API in identical state.

Coverage matrix:
  ✅ CREATE           POST /booking — field persistence, ID assignment
  ✅ READ             GET  /booking/{id} — round-trip data integrity
  ✅ LIST             GET  /booking — filter by name, immediate consistency
  ✅ UPDATE (full)    PUT  /booking/{id} — full replacement semantics
  ✅ UPDATE (partial) PATCH /booking/{id} — field isolation
  ✅ DELETE           DELETE /booking/{id} — confirmed absence
  ✅ DELETE idempotency — repeat DELETE returns 404, not 500
  ✅ EDGE: max-length fields — truncation regression guard
  ✅ EDGE: zero price       — boundary value for financial fields
  ✅ EDGE: unicode names    — international character encoding
  ✅ FULL LIFECYCLE         — CREATE → READ → UPDATE → VERIFY → DELETE → CONFIRM
"""
