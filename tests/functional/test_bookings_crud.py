@pytest.mark.crud
class TestUpdateBooking:

    def test_put_replaces_all_fields(
        self,
        created_booking: tuple[int, BookingPayload],
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN an existing booking created with payload A
        WHEN  PUT /booking/{id} is called with a completely different payload B
        THEN  GET /booking/{id} returns values from B, not A

        The critical assertion is the final negative: that original values are
        GONE. A system that merges instead of replacing is violating PUT
        semantics — a subtle bug that causes financial record corruption.
        """
        booking_id, original_payload = created_booking

        factory = BookingDataFactory()
        update_payload = factory.realistic(
            checkin_offset_days=60,
            stay_duration_days=7,
        )

        updated = booking_client.update_booking(booking_id, update_payload)

        # Assert new values are present
        assert updated.firstname == update_payload.firstname
        assert updated.lastname == update_payload.lastname
        assert updated.totalprice == update_payload.totalprice
        assert updated.depositpaid == update_payload.depositpaid
        assert updated.bookingdates.checkin == update_payload.bookingdates.checkin

        # Assert original values are GONE (distinguishes PUT from PATCH)
        assert updated.firstname != original_payload.firstname, (
            "PUT returned the original firstname — API may be merging instead "
            "of replacing. This is a PUT-semantics violation."
        )

        # Round-trip verification: re-fetch to confirm persistence
        re_fetched = booking_client.get_booking(booking_id)
        assert re_fetched.firstname == update_payload.firstname, (
            "PUT appeared to succeed but re-fetch returned stale data. "
            "Possible write-behind cache or transaction isolation issue."
        )
        assert re_fetched.totalprice == update_payload.totalprice

        logger.info(
            "assert_put_round_trip_passed",
            booking_id=booking_id,
            factory_seed=factory.seed,
        )


