@pytest.mark.crud
class TestCreateBooking:

    def test_create_booking_returns_positive_integer_id(
        self,
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a valid, Faker-generated booking payload
        WHEN  POST /booking is called
        THEN  the server returns a bookingid that is a positive integer
        AND   the response validates against the CreateBookingResponse contract

        Why this matters: a system that returns id=0, id=-1, or a string
        ID has a broken ID-generation sequence — a Tier-1 data integrity bug.
        """
        from conftest import deregister_from_cleanup, register_for_cleanup

        factory = BookingDataFactory()
        payload = factory.realistic()

        created = booking_client.create_booking(payload)
        register_for_cleanup(created.bookingid)

        try:
            assert isinstance(created.bookingid, int), (
                f"bookingid must be int, got {type(created.bookingid).__name__}"
            )
            assert created.bookingid > 0, (
                f"bookingid must be positive, got {created.bookingid}"
            )
            logger.info(
                "assert_create_id_valid",
                booking_id=created.bookingid,
                factory_seed=factory.seed,
            )
        finally:
            if booking_client.booking_exists(created.bookingid):
                booking_client.delete_booking(created.bookingid)
            deregister_from_cleanup(created.bookingid)

    def test_create_booking_persists_all_fields_exactly(
        self,
        created_booking: tuple[int, BookingPayload],
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a booking is created with a specific Faker-generated payload
        WHEN  GET /booking/{id} is called immediately after creation
        THEN  every field in the response matches the original payload exactly

        This is the canonical data-integrity test. Silent field truncation,
        type coercion, or default-value substitution are all bugs that this
        test is specifically designed to catch.
        """
        booking_id, payload = created_booking
        retrieved = booking_client.get_booking(booking_id)

        assert retrieved.firstname == payload.firstname, (
            f"firstname: expected '{payload.firstname}', got '{retrieved.firstname}'"
        )
        assert retrieved.lastname == payload.lastname, (
            f"lastname: expected '{payload.lastname}', got '{retrieved.lastname}'"
        )
        assert retrieved.totalprice == payload.totalprice, (
            f"totalprice: expected {payload.totalprice}, got {retrieved.totalprice}"
        )
        assert retrieved.depositpaid == payload.depositpaid, (
            f"depositpaid: expected {payload.depositpaid}, got {retrieved.depositpaid}"
        )
        assert retrieved.bookingdates.checkin == payload.bookingdates.checkin, (
            f"checkin: expected {payload.bookingdates.checkin}, "
            f"got {retrieved.bookingdates.checkin}"
        )
        assert retrieved.bookingdates.checkout == payload.bookingdates.checkout, (
            f"checkout: expected {payload.bookingdates.checkout}, "
            f"got {retrieved.bookingdates.checkout}"
        )
        assert retrieved.additionalneeds == payload.additionalneeds, (
            f"additionalneeds: expected '{payload.additionalneeds}', "
            f"got '{retrieved.additionalneeds}'"
        )

        logger.info(
            "assert_round_trip_integrity_passed",
            booking_id=booking_id,
            firstname=retrieved.firstname,
        )

    def test_create_booking_is_immediately_discoverable(
        self,
        created_booking: tuple[int, BookingPayload],
        booking_client: BookingClient,
    ) -> None:
        """
        GIVEN a booking is created with a UUID-unique lastname
        WHEN  GET /booking?lastname=<unique_value> is called
        THEN  the new booking ID appears in the results

        Tests write-then-read consistency. A system with eventual consistency
        or a write-behind cache may pass the create call but fail this test.
        In a financial system, that is a Tier-1 incident.
        """
        booking_id, payload = created_booking
        results = booking_client.list_bookings(
            firstname=payload.firstname,
            lastname=payload.lastname,
        )
        result_ids = [b.bookingid for b in results]

        assert booking_id in result_ids, (
            f"Created booking {booking_id} not found in filtered list. "
            f"Searched: firstname={payload.firstname!r}, lastname={payload.lastname!r}. "
            f"Found IDs: {result_ids}"
        )

